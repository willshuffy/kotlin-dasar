package Loops

fun main() {

    /*
        DoWhile = adalah perulangan yang melakukan eksekusi terlebih dahulu, baru setelah itu melakukan
        pengecekan kode blok, berbanding  terbalik dengan perulangan While
     */

    var i = 0

    do {
        println("perulangan ke $i")
        i++
    }while (i < 10)
}